	<h1>Introduction</h1>
	<p>
		[aside·eth] is a time-lock protocol for the contemporary digital art world, based on the collaboration of the <a
			href="https://distributedgallery.art"
			target="_blank">distributed gallery</a
		>, the <a href="https://verse.works" target="_blank">verse marketplace</a>,
		<a href="https://georgbak.art" target="_blank">Georg Bak</a>'s curation and various digital artists. It consists of a protocol that
		enables the immobilization of any NFT on the Ethereum blockchain and conditions its release to external phenomena, whether natural
		(forest, fishery, earthquake, storm), astral (solar eruption, planetary transit), economic (inflation, deflation), financial (stock
		market), demographic, or social. Once time-locked, these NFTs remain non-transferable for a dedicated period of time or until a specific
		real-world event happens, therefore restricting the tradability of the artwork by tying its commercial becoming to the outside world.
	</p>
	<p>
		[aside·eth] is about extracting NFTs from market mechanisms. It is about restoring a stillness outside the flow of exchange. It is about
		reclaiming a presence subtracted from its economic specters: aside from markets, aside from speculation, aside from value.
	</p>

	<h1>[aside·eth] and the flesh of the World</h1>
	<p>
		[aside·eth] aims to act as the stitching point between phenomena that are ordinarily separated, by linking artworks liquidity to outside
		world events, through the use of oracle technologies. Thus, the economic lifecycle of an artwork and the world's life become truly
		interdependent.
	</p>
	<p>
		[aside·eth] aims to drastically alter the experience of ownership by inoculating a consonant relationship with off chain realities into
		it. Thus, the more-than-human becomes the first and foremost owner of the artwork, that animates and haunts both this artwork and its
		economic owner.
	</p>

